def countdown(counter)
  
  while counter > 0 
    puts "#{counter} SECOND(S)!"
    counter -= 1 
  end

    "HAPPY NEW YEAR!"
end

def countdown_with_sleep
  y = 10 
  new_year = "HAPPY NEW YEAR"
  while y > 0 
    puts "#{y} SECOND(S)!"
    y -= 1 
    sleep 1 
  end
    new_year
end
